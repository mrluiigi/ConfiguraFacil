class ListOb{
    private int id;
    private String designacao;
    private float preco;
    
    public ListOb(int id, String d, float preco){
        this.id = id;
        this.designacao = d;
        this.preco = preco;
    }
    
    public String toString(){
        return this.designacao + "        " + this.preco + "€";
    }
    
    public int getId(){
        return this.id;
    }

    public float getPreco() {
        return preco;
    }
}